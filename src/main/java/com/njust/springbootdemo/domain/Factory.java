package com.njust.springbootdemo.domain;

public class Factory {

    private String Sid;
    private String Factoryname;
    private String Station;
    private String Roomnum;
    private String Room;
    private String Stationnum;
    private String Stationexplain;

    public String getSid() {
        return Sid;
    }

    public void setSid(String sid) {
        Sid = sid;
    }

    public String getFactoryname() {
        return Factoryname;
    }

    public void setFactoryname(String factoryname) {
        Factoryname = factoryname;
    }

    public String getStation() {
        return Station;
    }

    public void setStation(String station) {
        Station = station;
    }

    public String getRoomnum() {
        return Roomnum;
    }

    public void setRoomnum(String roomnum) {
        Roomnum = roomnum;
    }

    public String getRoom() {
        return Room;
    }

    public void setRoom(String room) {
        Room = room;
    }

    public String getStationnum() {
        return Stationnum;
    }

    public void setStationnum(String stationnum) {
        Stationnum = stationnum;
    }

    public String getStationexplain() {
        return Stationexplain;
    }

    public void setStationexplain(String stationexplain) {
        Stationexplain = stationexplain;
    }
}
